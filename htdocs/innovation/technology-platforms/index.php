    <p class="before_list">Организация технологической платформы «Освоение океана» заключается в создании совокупности «прорывных» технологий, которые сформируют инфраструктуру, обеспечивающую эффективную и комфортную деятельность человека в Мировом океане. Организационными задачами технологической платформы являются:</p>

    <ul class="list">
