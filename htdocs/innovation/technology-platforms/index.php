    	<li>привлечение организаций крупного бизнеса к участию в научных разработках и коммерциализации их результатов в области подводных технологий;</li>
    	<li>технологическая модернизация отечественной подводной робототехники;</li>
    	<li>внедрение новых технологий  в области разведки и добычи минеральных ресурсов Мирового океана;</li>
    	<li>создание системы комплексной защиты территорий, населения и критически важных объектов прибрежной зоны от угроз чрезвычайных ситуаций природного и техногенного характера;</li>
    	<li>обеспечение подготовки и переподготовки специалистов в системе высшего и среднего специального образования для работы в области освоения океана.</li>
